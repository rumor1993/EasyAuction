package com.easy.auction.model;

public class Cancel {
	private int ex_num;
	private String em_id;
	private String eb_itemnum;
	private String ex_reason;
	private String ex_date;
	
	public int getEx_num() {
		return ex_num;
	}
	public void setEx_num(int ex_num) {
		this.ex_num = ex_num;
	}
	public String getEm_id() {
		return em_id;
	}
	public void setEm_id(String em_id) {
		this.em_id = em_id;
	}
	public String getEb_itemnum() {
		return eb_itemnum;
	}
	public void setEb_itemnum(String eb_itemnum) {
		this.eb_itemnum = eb_itemnum;
	}
	public String getEx_reason() {
		return ex_reason;
	}
	public void setEx_reason(String ex_reason) {
		this.ex_reason = ex_reason;
	}
	public String getEx_date() {
		return ex_date;
	}
	public void setEx_date(String ex_date) {
		this.ex_date = ex_date;
	}
	

}
