package com.easy.auction.model;

public class Point {
	private int eg_num;
	private String eg_id;
	private String eg_name;
	private int eg_recoin;
	private int eg_orgcoin;
	private String eg_state;
	private String eg_date;
	private String eg_apply;
	public int getEg_num() {
		return eg_num;
	}
	public void setEg_num(int eg_num) {
		this.eg_num = eg_num;
	}
	public String getEg_id() {
		return eg_id;
	}
	public void setEg_id(String eg_id) {
		this.eg_id = eg_id;
	}
	public String getEg_name() {
		return eg_name;
	}
	public void setEg_name(String eg_name) {
		this.eg_name = eg_name;
	}
	public int getEg_recoin() {
		return eg_recoin;
	}
	public void setEg_recoin(int eg_recoin) {
		this.eg_recoin = eg_recoin;
	}
	public int getEg_orgcoin() {
		return eg_orgcoin;
	}
	public void setEg_orgcoin(int eg_orgcoin) {
		this.eg_orgcoin = eg_orgcoin;
	}
	public String getEg_state() {
		return eg_state;
	}
	public void setEg_state(String eg_state) {
		this.eg_state = eg_state;
	}
	public String getEg_date() {
		return eg_date;
	}
	public void setEg_date(String eg_date) {
		this.eg_date = eg_date;
	}
	public String getEg_apply() {
		return eg_apply;
	}
	public void setEg_apply(String eg_apply) {
		this.eg_apply = eg_apply;
	}
	
}
