package com.easy.auction.model;

public class Letter {
	private int el_num;
	private String eb_itemnum;
	private String el_request;
	private String el_response;
	private String el_message;
	private String el_confirm;
	private String el_date;
	public int getEl_num() {
		return el_num;
	}
	public void setEl_num(int el_num) {
		this.el_num = el_num;
	}
	public String getEb_itemnum() {
		return eb_itemnum;
	}
	public void setEb_itemnum(String eb_itemnum) {
		this.eb_itemnum = eb_itemnum;
	}
	public String getEl_request() {
		return el_request;
	}
	public void setEl_request(String el_request) {
		this.el_request = el_request;
	}
	public String getEl_response() {
		return el_response;
	}
	public void setEl_response(String el_response) {
		this.el_response = el_response;
	}
	public String getEl_message() {
		return el_message;
	}
	public void setEl_message(String el_message) {
		this.el_message = el_message;
	}
	public String getEl_confirm() {
		return el_confirm;
	}
	public void setEl_confirm(String el_confirm) {
		this.el_confirm = el_confirm;
	}
	public String getEl_date() {
		return el_date;
	}
	public void setEl_date(String el_date) {
		this.el_date = el_date;
	}
	
}
