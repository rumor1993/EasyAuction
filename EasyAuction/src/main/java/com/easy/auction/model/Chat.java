package com.easy.auction.model;

public class Chat {
	private int ec_num;
	private String ec_request;
	private String ec_response;
	private String ec_content;
	private String ec_date;
	public int getEc_num() {
		return ec_num;
	}
	public void setEc_num(int ec_num) {
		this.ec_num = ec_num;
	}
	public String getEc_request() {
		return ec_request;
	}
	public void setEc_request(String ec_request) {
		this.ec_request = ec_request;
	}
	public String getEc_response() {
		return ec_response;
	}
	public void setEc_response(String ec_response) {
		this.ec_response = ec_response;
	}
	public String getEc_content() {
		return ec_content;
	}
	public void setEc_content(String ec_content) {
		this.ec_content = ec_content;
	}
	public String getEc_date() {
		return ec_date;
	}
	public void setEc_date(String ec_date) {
		this.ec_date = ec_date;
	}
	
}
